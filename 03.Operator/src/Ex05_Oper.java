
public class Ex05_Oper {
	public static void main(String[] args) {
		int result =0;
		//result 에 10을 더한 결과를 result에 할당
		result = result+10; // result = 0 + 10
		result = result+10;
		System.out.println(result);
		
		result+=10;
		result+=10;//복합대입연산자
		
		result = result +1000;//이 후로는 result 가 1040의 값을 가진다.
		System.out.println(result+10);
		System.out.println(result);
		
		
				
		
	}

}
